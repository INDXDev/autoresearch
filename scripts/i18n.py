"""Tiny i18n layer for the Auto Research template.

Language is chosen with the ``OUTPUT_LANGUAGE`` environment variable (set via a
GitHub Actions Variable or Secret). Supported values:

* ``en`` (default) — English
* ``ja`` — Japanese / 日本語

Anything unrecognised falls back to English.

Claude (in the research step) writes the *content* in the chosen language; this
module only localises the deterministic Markdown scaffolding that the Python
publisher wraps around that content (headings, field labels, the Slack line).
"""

from __future__ import annotations

import os

DEFAULT_LANGUAGE = "en"
SUPPORTED = ("en", "ja")


def get_language() -> str:
    """Return the normalised output language code (``en`` or ``ja``)."""
    raw = os.environ.get("OUTPUT_LANGUAGE", "").strip().lower()
    if raw in {"ja", "jp", "japanese", "日本語"}:
        return "ja"
    if raw in {"en", "english"}:
        return "en"
    return DEFAULT_LANGUAGE



def t(key: str, **kwargs: object) -> str:
    """Return the localised string for ``key``, formatted with ``kwargs``."""
    lang = get_language()
    entry = _STRINGS[key]
    # Distinguish a missing language from an intentionally empty string (``""``
    # is falsy, so ``or`` would wrongly fall back to English for it).
    template = entry[lang] if lang in entry else entry[DEFAULT_LANGUAGE]
    return template.format(**kwargs) if kwargs else template
